package admin;

public class CheckDataBean {
	private String stu_name;
	private int time;
	private String stu_code;
	private int stu_enter;
	
	
	public String getStu_name() {
		return stu_name;
	}
	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public String getStu_code() {
		return stu_code;
	}
	public void setStu_code(String stu_code) {
		this.stu_code = stu_code;
	}
	public int getStu_enter() {
		return stu_enter;
	}
	public void setStu_enter(int stu_enter) {
		this.stu_enter = stu_enter;
	}
	


}
